// –í —Ñ—É–Ω–∫—Ü–∏–∏ contains –º—ã –±—É–¥–µ–º –ø—Ä–æ–≤–µ—Ä—è—Ç—å,
// —Å–æ–¥–µ—Ä–∂–∏—Ç—Å—è –ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–π –∑–∞–ø—Ä–æ—Å
// –≤ –∫–∞–∫–æ–º-–ª–∏–±–æ –∏–∑ –Ω–∞–∑–≤–∞–Ω–∏–π:
function contains(query) {
  return pizzaList.filter((title) =>
    title.toLowerCase().includes(query.toLowerCase())
  );
}

